-- QUERIES
-- Overpaid invoices
SELECT
    name CustomerName,
    email CustomerEmail,
    phoneNumber CustomerPhoneNumber,
    invoiceID InvoiceNumber,
    amountPaid-amountOwed AmountOverpaid
FROM Invoice
JOIN PrimaryContact ON Invoice.billedTo = PrimaryContact.customerID
WHERE
    amountOwed < amountPaid AND
    amountOwed > 0;

-- Overdue invoices
SELECT
    name CustomerName,
    email CustomerEmail,
    phoneNumber CustomerPhoneNumber,
    invoiceID InvoiceNumber,
    amountOwed-amountPaid AmountOwing
FROM Invoice
JOIN PrimaryContact ON Invoice.billedTo = PrimaryContact.customerID
WHERE
    amountOwed > amountPaid AND
    dateIssued+30 < CURRENT_DATE AND
    amountOwed > 0;

-- Acount balances
SELECT 
    PrimaryContact.customerID CustomerID,
    PrimaryContact.name CustomerName,
    SUM(Invoice.amountOwed) AmountOwed, 
    SUM(Invoice.amountPaid) AmountPaid,
    SUM(Invoice.amountOwed)-SUM(Invoice.amountPaid) Balance
FROM Invoice
JOIN PrimaryContact ON Invoice.billedTo = PrimaryContact.customerID
GROUP BY 
    PrimaryContact.customerID,
    PrimaryContact.name
ORDER BY PrimaryContact.customerID;

-- Owners of multiple locations
SELECT
    PrimaryContact.name CustomerName,
    Region.name RegionName,
    COUNT(*) LocationsOwned
FROM ServicedLocation
JOIN PrimaryContact ON ServicedLocation.primaryContact = PrimaryContact.customerID
JOIN Region ON ServicedLocation.regionID = Region.regionID
GROUP BY 
    PrimaryContact.name,
    Region.name
HAVING COUNT(*) > 1
ORDER BY -COUNT(*);

-- Last quarter finances
SELECT
    Region.regionID RegionID,
    Region.name Name,
    SUM(FacilityTransaction.cost) ExportProfit
FROM
    FacilityTransaction,
    Region,
    ServicedLocation,
    SuppliesRegion
WHERE
    FacilityTransaction.facilityID = SuppliesRegion.facilityID AND
    SuppliesRegion.regionID = Region.regionID AND
    FacilityTransaction.isImport = 0 AND
    FacilityTransaction.dateCreated >= '01-SEP-2021' AND
    FacilityTransaction.dateCreated < '31-DEC-2021'
GROUP BY 
    Region.regionID, 
    Region.name
ORDER BY Region.regionID;

-- Net income of facilities
SELECT
    PowerFacility.facilityID FacilityID,
    PowerFacility.name FacilityName,
    SUM(CASE WHEN FacilityTransaction.isImport = 0 THEN FacilityTransaction.cost END) ExportCost,
    SUM(CASE WHEN FacilityTransaction.isImport = 1 THEN FacilityTransaction.cost END) ImportCost,
    (
        SUM(CASE WHEN FacilityTransaction.isImport = 0 THEN FacilityTransaction.cost END)
        -
        SUM(CASE WHEN FacilityTransaction.isImport = 1 THEN FacilityTransaction.cost END)
    ) Profit
FROM PowerFacility
JOIN FacilityTransaction ON PowerFacility.facilityID = FacilityTransaction.facilityID
WHERE
    FacilityTransaction.dateCreated >= '01-SEP-2021' AND
    FacilityTransaction.dateCreated < '31-DEC-2021'
GROUP BY 
    PowerFacility.facilityID, 
    PowerFacility.name
ORDER BY PowerFacility.facilityID;

