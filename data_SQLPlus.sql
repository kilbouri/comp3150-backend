-- represents the primary contact info for a serviced location (aka a customer)
CREATE TABLE PrimaryContact (
    customerID      INT,                    -- a numeric ID for this customer
    email           VARCHAR(64),            -- an optional email for the customer
    phoneNumber     VARCHAR(64) NOT NULL,   -- a non-optional phone number for the customer
    name            VARCHAR(64) NOT NULL,   -- the name of the customer
    --
    PRIMARY KEY (customerID)
);

-- a large area in which many customers may be located and is served by 1+ facilities
CREATE TABLE Region (
    regionID        INT,                    -- a numeric ID for this region
    name            VARCHAR(64),            -- the name of this region, such as "Ontario"
    unitCost        REAL NOT NULL,          -- the unit cost of power in this region, per kWh
    --
    PRIMARY KEY (regionID)
);

-- a facility which generates power given some inputs
CREATE TABLE PowerFacility (
    facilityID      INT,                    -- a numeric ID for the facility
    name            VARCHAR(64),            -- the name of the facility, such as "Bruce Power Unit 3"
    address         VARCHAR(64) NOT NULL,   -- the address of the facility, such as "123 Electric Way"
    volumeGenerated REAL NOT NULL,          -- the maximum amount of power this facility can supply (in MW), such as 950.50
    energyForm      VARCHAR(64) NOT NULL,   -- the form of energy used to produce power, such as "Nuclear "Oil or "Solar".
    --
    PRIMARY KEY (facilityID),
    --
    CHECK (volumeGenerated >= 0)
);

-- Represents a M:N relation between PowerFacility and Region
CREATE TABLE SuppliesRegion (
    facilityID      INT,                    -- the numeric ID of the supplying facility
    regionID        INT,                    -- the numeric ID of the supplied region
    --
    PRIMARY KEY (facilityID, regionID),
    FOREIGN KEY (facilityID) REFERENCES PowerFacility(facilityID),
    FOREIGN KEY (regionID) REFERENCES Region(regionID)
);

-- Represents a location within a region that the company supplies power to
CREATE TABLE ServicedLocation (
    locationID      INT,                    -- a numeric ID for the serviced location
    energyUsed      REAL NOT NULL,          -- the amount of energy used by the location, in kWh
    address         VARCHAR(64) NOT NULL,   -- the street address of the serviced location
    primaryContact  INT NOT NULL,           -- the numeric ID of the location's primary contact
    regionID        INT NOT NULL,           -- the region the location is part of
    --
    PRIMARY KEY (locationID, primaryContact),
    FOREIGN KEY (primaryContact) REFERENCES PrimaryContact(customerID),
    FOREIGN KEY (regionID) REFERENCES Region(regionID),
    --
    CHECK (energyUsed >= 0)
);

-- Represents a bill sent to a customer for the ServicedLocation(s) they are primary contact for
CREATE TABLE Invoice (
    invoiceID       INT,                    -- a numeric ID for this invoice
    dateIssued      DATE NOT NULL,          -- the date the invoice was issued
    amountOwed      NUMERIC(12, 2) NOT NULL,-- the dollar amount owed by the customer
    amountPaid      NUMERIC(12, 2) NOT NULL,-- the dollar amount paid by the customer
    billedTo        INT NOT NULL,           -- the numeric ID of the customer billed by this invoice
    --
    PRIMARY KEY (invoiceID),
    FOREIGN KEY (billedTo) REFERENCES PrimaryContact(customerID)
);

-- Represents an export (or import, if isImport = true) for a PowerFacility
CREATE TABLE FacilityTransaction (
    expenseID       INT,                    -- a numeric ID for the expense/import
    cost            REAL NOT NULL,          -- the absolute cost of the expense/import
    isImport        SMALLINT NOT NULL,      -- indicates whether this was an import or export (true = import)
    reason          VARCHAR(64),            -- an optional textual reason for the expense, such as "Purchased fissile material"
    dateCreated     DATE,                   -- when the transaction was created
    facilityID      INT,                    -- the numeric ID of the facility that "created" this expense/import
    --
    PRIMARY KEY (expenseID),
    FOREIGN KEY (facilityID) REFERENCES PowerFacility(facilityID),
    --
    CHECK (cost >= 0)
);

